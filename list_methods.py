# sort : means increasing order
l1 = [1,4,7,6,8,4]
# l_sorted = l1.sort()
# print(l_sorted)
l1.sort()
print(l1)

# reverse
l2 = [1,4,7,6,8,4]
l2.reverse()
print(l2)

# append : means add anything
l3 = [1,4,7,6,8,4]
l3.append(45)
l3.append(65)
print(l3)

# Insert
l4 = [1,4,7,6,8,4]
l4.insert(1, 72)
print(l4)

# pop
l5 = [1,4,7,6,8,4]
l5.pop(5)
print(l5)

# remove
l6 = [1,4,7,6,8,4]
l6.remove(8)
print(l6)